#User Input
# The input() function allows for user input. The input function takes a single argument, which is the prompt message. The prompt message is displayed to the user, and the program waits for the user to enter a value. The value entered by the user is returned by the input function.
name = input("Please enter your name: ")
age = input("Please enter your age: ")
location = input("Please enter your location: ")

# Print a personalized message
print(f"Hello {name}, you are {age} years old and live in {location}.")
